document.addEventListener('DOMContentLoaded', () => {
    // Internationalization (i18n) Logic
    const langToggle = document.getElementById('langToggle');
    let currentLang = 'fr'; // Default language is French

    if (langToggle) {
        langToggle.addEventListener('click', () => {
            currentLang = currentLang === 'fr' ? 'en' : 'fr';
            langToggle.textContent = currentLang === 'fr' ? 'EN' : 'FR';
            
            document.documentElement.lang = currentLang;
            
            // Update all translatable elements
            document.querySelectorAll('[data-i18n]').forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (translations[currentLang][key]) {
                    if (key === 'title') {
                        document.title = translations[currentLang][key];
                    } else {
                        el.innerHTML = translations[currentLang][key];
                    }
                }
            });
        });
    }

    // Navbar Scroll Effect
    const navbar = document.querySelector('.navbar');
    
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // Booking Modal Logic
    const bookingModal = document.getElementById('bookingModal');
    const closeModalBtn = document.getElementById('closeModal');
    const bookingForm = document.getElementById('bookingForm');
    const activitySelect = document.getElementById('b-activity');
    
    // Open modal buttons
    const openModalBtns = document.querySelectorAll('.open-booking-btn');
    openModalBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault(); // Prevent jump to #contact
            
            // If the card has a data-activity attribute, pre-select it
            const activity = btn.getAttribute('data-activity');
            if (activity) {
                // Find matching option by text or value
                Array.from(activitySelect.options).forEach(opt => {
                    if(opt.value === activity || opt.text === activity || (translations['fr'][`modal_opt_${activity.toLowerCase().replace(/ /g, '_')}`] === activity)){
                         activitySelect.value = opt.value;
                    }
                });
            } else {
                activitySelect.value = "";
            }
            
            // Set default date to today
            const today = new Date().toISOString().split('T')[0];
            document.getElementById('b-date').value = today;
            
            bookingModal.classList.add('active');
            document.body.style.overflow = 'hidden'; // Prevent scrolling
        });
    });

    // Close modal
    const closeModal = () => {
        bookingModal.classList.remove('active');
        document.body.style.overflow = 'auto'; // Restore scrolling
    };

    if (closeModalBtn) {
        closeModalBtn.addEventListener('click', closeModal);
    }

    // Close on click outside
    window.addEventListener('click', (e) => {
        if (e.target === bookingModal) {
            closeModal();
        }
    });

    // Form Submission -> WhatsApp
    if (bookingForm) {
        bookingForm.addEventListener('submit', (e) => {
            e.preventDefault();
            
            const activity = document.getElementById('b-activity').value;
            const date = document.getElementById('b-date').value;
            const time = document.getElementById('b-time').value;
            const persons = document.getElementById('b-persons').value;
            const name = document.getElementById('b-name').value;
            
            // Format the WhatsApp message
            const message = `Hello! I would like to book a session.%0A%0A*Activity:* ${activity}%0A*Date:* ${date}%0A*Time:* ${time}%0A*Persons:* ${persons}%0A*Name:* ${name}%0A%0APlease let me know if this slot is available.`;
            
            // Open WhatsApp with prefilled message
            const whatsappNumber = "41786586634";
            const whatsappUrl = `https://wa.me/${whatsappNumber}?text=${message}`;
            
            window.open(whatsappUrl, '_blank');
            closeModal();
            bookingForm.reset();
        });
    }

    // Scroll Animation Observer
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.15
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                // Optional: Stop observing once animated to keep it visible
                // observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    const animatedElements = document.querySelectorAll('.fade-in-up, .fade-in-left, .fade-in-right');
    animatedElements.forEach(el => observer.observe(el));

    // Number Counter Animation
    const counterElements = document.querySelectorAll('.counter-up h3');
    let hasCounted = false;

    const countUp = () => {
        counterElements.forEach(counter => {
            const target = parseInt(counter.innerText.replace(/\D/g, ''));
            const duration = 2000; // ms
            const stepTime = Math.abs(Math.floor(duration / target));
            let current = 0;
            const suffix = counter.innerText.replace(/[0-9]/g, '');

            const timer = setInterval(() => {
                current += Math.ceil(target / (duration / 50));
                if (current >= target) {
                    current = target;
                    clearInterval(timer);
                }
                counter.innerText = current + suffix;
            }, 50);
        });
    };

    const statsSection = document.querySelector('.stats-container');
    
    if (statsSection) {
        const statsObserver = new IntersectionObserver((entries) => {
            if (entries[0].isIntersecting && !hasCounted) {
                countUp();
                hasCounted = true;
            }
        });
        statsObserver.observe(statsSection);
    }

    // Hero Slider
    const slides = document.querySelectorAll('.slide');
    const dots = document.querySelectorAll('.dot');
    const prevBtn = document.querySelector('.prev-slide');
    const nextBtn = document.querySelector('.next-slide');
    
    if (slides.length > 0) {
        let currentSlide = 0;
        let slideInterval;
        
        const initSlides = () => {
            slides.forEach((slide, index) => {
                const elements = slide.querySelectorAll('.slide-fade-up');
                if (index === currentSlide) {
                    slide.classList.add('active');
                    setTimeout(() => {
                        elements.forEach(el => el.classList.add('visible'));
                    }, 100);
                } else {
                    slide.classList.remove('active');
                    elements.forEach(el => el.classList.remove('visible'));
                }
            });
            updateDots();
        };

        const updateDots = () => {
            dots.forEach((dot, index) => {
                dot.classList.toggle('active', index === currentSlide);
            });
        };

        const goToSlide = (n) => {
            slides[currentSlide].querySelectorAll('.slide-fade-up').forEach(el => el.classList.remove('visible'));
            slides[currentSlide].classList.remove('active');
            
            currentSlide = (n + slides.length) % slides.length;
            
            slides[currentSlide].classList.add('active');
            setTimeout(() => {
                slides[currentSlide].querySelectorAll('.slide-fade-up').forEach(el => el.classList.add('visible'));
            }, 100);
            
            updateDots();
            resetInterval();
        };

        const nextSlide = () => goToSlide(currentSlide + 1);
        const prevSlide = () => goToSlide(currentSlide - 1);

        if (nextBtn) nextBtn.addEventListener('click', nextSlide);
        if (prevBtn) prevBtn.addEventListener('click', prevSlide);

        dots.forEach((dot, index) => {
            dot.addEventListener('click', () => {
                goToSlide(index);
            });
        });
        
        const resetInterval = () => {
            clearInterval(slideInterval);
            slideInterval = setInterval(nextSlide, 5000); // 5 seconds per slide
        };

        initSlides();
        resetInterval();
    }
});
